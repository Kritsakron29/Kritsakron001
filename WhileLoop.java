public class WhileLoop {
    public static void main(String[] args) {
        for(int i =1; i <= 100; i++){
            System.err.println(+i);

        }

        int iWhile = 1;
        while (iWhile <= 100) {
            System.err.println("result in whileloop : "+iWhile);
            iWhile ++ ;
        }
    }
}
